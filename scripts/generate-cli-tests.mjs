#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const packageRoot = path.resolve(path.dirname(new URL(import.meta.url).pathname), '..');
const sourcePath = path.join(packageRoot, 'src', 'universal-commands.ts');
const outputPath = path.join(packageRoot, 'tests', 'generated', 'universal-cli.generated.test.ts');

const source = fs.readFileSync(sourcePath, 'utf8');
const matches = [...source.matchAll(/export const\s+(\w+Command)\s*=\s*new UniversalCommand/g)];

if (matches.length === 0) {
  throw new Error('No UniversalCommand exports found for generated CLI tests');
}

const exportsList = matches.map((m) => m[1]);

const content = `/* eslint-disable */
// AUTO-GENERATED by scripts/generate-cli-tests.mjs
// Do not edit manually.

import { describe, expect, it } from 'vitest';
import { ${exportsList.join(', ')} } from '../../src/universal-commands.js';

const commands = [${exportsList.join(', ')}];

describe('generated universal command CLI tests', () => {
  for (const command of commands) {
    it(\`\${command.schema.name} exposes CLI/MCP surface\`, () => {
      const cli = command.toCLI();
      expect(cli).toBeTruthy();

      const mcp = command.toMCP();
      expect(mcp).toBeTruthy();
      expect(typeof mcp.name).toBe('string');
      expect(mcp.name.length).toBeGreaterThan(0);
      expect(typeof command.schema.name).toBe('string');
      expect(command.schema.name.length).toBeGreaterThan(0);
    });
  }
});
`;

fs.writeFileSync(outputPath, content);
console.log(`Generated ${outputPath}`);
